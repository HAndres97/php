# 5-FUNCIONES DE CARACTERES
# CONVERTIR CARACTERES EN MAYUSCULA
/*
    UPPER(NOMBRE)
    LOWER(APELLIDOS)
    CONCAT(NOMBRE, ' ',APELLIDOS)
*/
SELECT CONCAT(NOMBRE,' ',APELLIDOS) AS 'NOMBRE_COMPLETO' FROM USUARIOS ORDER BY EMAIL DESC;

# PODEMOS COLOCAR FUNCIONES DENTRO DE OTRAS
SELECT UPPER(CONCAT(NOMBRE,' ',APELLIDOS)) AS 'NOMBRE_COMPLETO' FROM USUARIOS ORDER BY EMAIL DESC;

# FUNCION LENGTH, NUMERO DE CARACTERES DE UN TEXTO OJO CUENTA EL ESPACIO TAMBIEN
SELECT CONCAT(NOMBRE,' ',APELLIDOS) AS 'NOMBRE_COMPLETO', LENGTH(CONCAT(NOMBRE,' ',APELLIDOS)) AS 'LONGITUD_NOMBRE' FROM USUARIOS ORDER BY EMAIL DESC;

