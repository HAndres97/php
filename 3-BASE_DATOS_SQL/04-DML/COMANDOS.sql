# 6-FUNCIONES DE FECHA
# CURDATE() TE MUESTRA LA FECHA ACTUAL
SELECT NOMBRE, FECHA, CURDATE() AS 'FECHA_ACTUAL' FROM USUARIOS ORDER BY ID DESC;

# DIFERENCIA DE FECHAS DATEDIFF, TE DICE LOS DIAS QUE HAY DE DIFERENCIA
SELECT NOMBRE, FECHA, DATEDIFF(FECHA,CURDATE()) AS 'DIFERENCIA_FECHA' FROM USUARIOS ORDER BY ID DESC;
/* 
    DAYNAME(FECHA) SACA EL NOMBRE DEL DIA
    DAYMONTH(FECHA) NOMBRE DEL MES
    DAYOFWEEK(FECHA) NUM DE LA SEMANA 1-7
    DAYOFYEAR(FECHA) DIA DEL AÑO
    MONTH(FECHA) NUMERO DEL MES
    YEAR(FECHA) AÑO
    HOUR/MINUTE/SECOND(FECHA) QUE PODEMOS SACAR
    SYSDATE() HORA Y FECHA DEL ORDENADOR
*/
# CURTIME SACA LA HORA EN LA QUE ESTAMOS

SELECT NOMBRE, CURTIME() AS 'HORA' FROM USUARIOS ORDER BY ID;

# FORMATEAR FECHA DATE_FORMAT(FECHA,'%d-%m-%y')

SELECT NOMBRE,DATE_FORMAT(FECHA,'%d-%m-%y') AS 'FECHA EUROPEA' FROM USUARIOS ORDER BY ID;
