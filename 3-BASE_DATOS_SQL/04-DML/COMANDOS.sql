/*
    SUM(), SUMA ELEMENTOS DE UN COLUMNA
    ABS()N.ABSOLUTO
    CEIL()REDONDEO
    FLOOR()REDONDEO A LA BAJA
    RAND() NUMERO ALEATORIO
    BUSCAR SQL FUNCTIONS MATH Y TE SALEN LAS QUE HAY
    ROUND(7.765,2) TE SACA EL NUMERO CON LOS DECIMALES QUE LE DIGAS
    TRUNCATE(7.765,2)QUITAR DECIMALES, PARECIDO A ROUND
