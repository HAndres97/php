# PUEDE SER DESCENDENTE O ASCENDENTE
SELECT ID,NOMBRE,EMAIL, (4 + 7) AS 'OPERACION' FROM USUARIOS ORDER BY ID;
SELECT ID,NOMBRE,EMAIL, (4 + 7) AS 'OPERACION' FROM USUARIOS ORDER BY ID DESC;
SELECT ID,NOMBRE,EMAIL, (4 + 7) AS 'OPERACION' FROM USUARIOS ORDER BY ID ASC;

